Args:
    --class-section
        The section where the class will be saved.
        One of:
            - haddock
            - plug-ins
            - project-specific
    --class-module
        The module where the class will be saved.
        If the class-section has been set to 'project-specific'
        this must not be set.
    --class-name
        The name of the class.
