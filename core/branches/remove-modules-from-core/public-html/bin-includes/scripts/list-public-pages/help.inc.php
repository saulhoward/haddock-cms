Args:
    --search-section
        The section that will be searched.
        One of:
            - haddock
            - plug-ins
            - project-specific
    --search-module
        The module that will be searched.
        If the search-section has been set to 'project-specific'
        this must not be set.
    --search-type
        The type of page to search for.
