A script to gather a bit of info about an
SVN project and how it's branching and merging.

@ Clear Line Web Design, 2007-04-26

Usage:
    --project-root
        The top level directory for this project.
        It might contain these folders:
            trunk
            branches
            tags
        
    --help

Intended Use:
    The idea of this script is that the whole of
    the clwd-repos is checked out to a large working
    directory on a work station.
    A developer can use this to inspect the various
    branches of the different projects.
